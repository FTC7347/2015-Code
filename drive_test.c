#include "JoystickDriver.c"

const int driveType = 1;

task main()
{
	while(true)
	{
		//each stick controls a motor
		getJoystickSettings(joystick);

		if (driveType == 0) {

			if(joystick.joy1_y1<20 && joystick.joy1_y1>-20){
				motor[left] = 0;
				motor[left2] = 0;
				}else{
				motor[left] = -joystick.joy1_y1;
				motor[left2] = joystick.joy1_y1;
			}
			if(joystick.joy1_y2<20 && joystick.joy1_y2>-20){
				motor[right] = 0;
				motor[right2] = 0;
				}else{
				motor[right] = joystick.joy1_y2;
				motor[right2] = joystick.joy1_y2;
			}
			} else if (driveType == 1) {
			int leftIn = joystick.joy1_y1;
			int leftPwr = leftIn * leftIn / 127;
			if (leftIn < 0) {
				leftPwr = leftPwr * -1;
			}
			motor[left] = -leftPwr;
			motor[left2] = leftPwr;
			int rightIn = joystick.joy1_y2;
			int rightPwr = rightIn * rightIn / 127;
			if (rightIn < 0) {
				rightPwr = rightPwr * -1;
			}
			motor[right] = rightPwr;
			motor[right2] = rightPwr;
		}
		if(joy1Btn(6)){
			motor[intake] = -100;
			motor[launcher] = 100;
			}else if(joy1Btn(8)){
			motor[intake] = 100;
			motor[launcher] = -100;
			}else{
			motor[intake] = 0;
			motor[launcher] = 0;
		}
		if(joy1Btn(2)){
			servo[grabber] = 90;
		}else if(joy1Btn(1)){
			servo[grabber] = 180;
		}else{
			servo[grabber] = 127;
		}

		//added jan 7
		if(joy1Btn(5)){
			motor[lifter] = 100;
		}else if(joy1Btn(7)){
			motor[lifter] = -100;
		}
		else{
			motor[lifter] = 0;
		}
	}
}
